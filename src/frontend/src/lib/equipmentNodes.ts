import type {
  CleaningLevel,
  CleaningLogEntry,
  CleaningRule,
  ComparisonType,
  GmpStatus,
  HealthStatus,
  MaintenanceStatus,
} from "./gmpValidation";

export type EntityType =
  | "WorkCenter"
  | "Station"
  | "EquipmentClass"
  | "EquipmentEntity"
  | "PropertyType";

export interface ChangeEntry {
  timestamp: string;
  field: string;
  oldValue: string;
  newValue: string;
  changedBy: string;
  action?: "Create" | "Update" | "Delete";
  reason?: string;
}

export interface EquipmentNode {
  id: string;
  identifier: string;
  shortDescription: string;
  description: string;
  level: string;
  inventoryNumber: string;
  manufacturer: string;
  externalId: string;
  serialNumber: string;
  manufacturingDate: string;
  disposed: boolean;
  barcode: string;
  barcodeEnabled: boolean;
  entityType: EntityType;
  parentId: string | null;
  automationServerNameDefault: string;
  automationServerName: string;
  dataOpsPath: string;
  historianProvider: string;
  historianAccessServerDefault: string;
  historianAccessServer: string;
  historianServerDefault: string;
  historianServer: string;
  createdAt: string;
  changeHistory: ChangeEntry[];
  // GMP fields
  status?: GmpStatus;
  maintenance_status?: MaintenanceStatus;
  health_status?: HealthStatus;
  last_maintenance_date?: string;
  pm_due_date?: string;
  cleaningRules?: CleaningRule[];
  cleaningLog?: CleaningLogEntry | null;
  // PropertyType capability fields
  min_value?: string;
  max_value?: string;
  required_min?: string;
  required_max?: string;
  comparison_type?: ComparisonType | "";
  // Change Control fields
  versionNumber?: number;
  originalId?: string | null;
  revisedById?: string | null;
  changeControlReason?: string;
  // Lifecycle & Audit fields (added for MES refactor)
  createdBy?: string; // default 'System'
  updatedAt?: string; // ISO string
  isUsedInBatch?: boolean; // default false — locks Edit/Delete when true
}

